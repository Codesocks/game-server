public class Player {
    private String name;
    private boolean isComputer;

    public String getName() {
        return name;
    }

    public boolean isComputer() {
        return isComputer;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setComputer(boolean computer) {
        isComputer = computer;
    }
}
